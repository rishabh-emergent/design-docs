def judge_one_playbook(integration, provider, playbook_text):
    """Call Claude Opus 4.7 to score a single playbook."""
    pid = f"{integration['id']}-{provider}"
    user_msg = f"""INTEGRATION: {integration['name']}
TEMPLATE STACK: {', '.join(integration['tech_stack'])}
CANONICAL CONSTRAINTS:
{integration['constraints']}

OFFICIAL DOCS REFERENCE: docs_unavailable=true (apply LONG-TAIL HANDLING clause)

PLAYBOOK_ID: {pid}

PLAYBOOK:
{playbook_text}"""

    body = {
        "model": "claude-opus-4-7",
        "max_tokens": 4096,
        # NOTE: temperature must be omitted for opus 4.7 — setting it returns 400
        "system": JUDGE_SYSTEM_PROMPT,
        "messages": [{"role": "user", "content": user_msg}],
    }
    t0 = time.time()
    try:
        data = json.dumps(body).encode("utf-8")
        req = urllib.request.Request(
            "https://api.anthropic.com/v1/messages",
            data=data,
            headers={
                "x-api-key": ANTHROPIC_KEY,
                "anthropic-version": "2023-06-01",
                "content-type": "application/json",
            },
            method="POST",
        )
        with urllib.request.urlopen(req, timeout=300) as resp:
            api = json.loads(resp.read().decode("utf-8"))
        content_blocks = api.get("content", [])
        text = "".join(b.get("text", "") for b in content_blocks if b.get("type") == "text").strip()
        # Strip code fences if any leaked
        if text.startswith("```"):
            text = "\n".join(text.split("\n")[1:])
            if text.endswith("```"):
                text = "\n".join(text.split("\n")[:-1])
        parsed = json.loads(text)
        parsed["_judge_latency_s"] = time.time() - t0
        parsed["_judge_input_tokens"] = api.get("usage", {}).get("input_tokens")
        parsed["_judge_output_tokens"] = api.get("usage", {}).get("output_tokens")
        return parsed
    except Exception as e:
        return {
            "error": str(e),
            "_judge_latency_s": time.time() - t0,
            "playbook_id": pid,
        }


def pairwise_winner(score_a, score_b):
    """Local computation per TIE RULES in rubric:
       - TIE if sum |dim_diff| ≤ 2 AND no single dim differs by ≥ 2.
       - If a single dim differs by ≥ 2, that dim alone decides.
       - both_broken if EITHER has correctness_and_freshness ≤ 2.
    """
    dims = ["correctness_and_freshness", "executability", "completeness", "citation_anchoring"]
    a = {d: score_a.get(d, 0) for d in dims}
    b = {d: score_b.get(d, 0) for d in dims}
    diffs = {d: a[d] - b[d] for d in dims}
    sum_abs = sum(abs(v) for v in diffs.values())
    max_abs_dim = max(diffs, key=lambda d: abs(diffs[d]))
    max_abs = abs(diffs[max_abs_dim])

    both_broken = a["correctness_and_freshness"] <= 2 or b["correctness_and_freshness"] <= 2

    if max_abs >= 2:
        # decisive dimension
        winner = "A" if diffs[max_abs_dim] > 0 else "B"
        return {
            "winner": winner,
            "decisive_dim": max_abs_dim,
            "decisive_diff": diffs[max_abs_dim],
            "all_diffs": diffs,
            "sum_abs": sum_abs,
            "both_broken": both_broken,
            "tie_rule": "single_dim_ge_2",
        }
    if sum_abs <= 2:
        return {
            "winner": "TIE",
            "all_diffs": diffs,
            "sum_abs": sum_abs,
            "both_broken": both_broken,
            "tie_rule": "within_noise",
        }
    # No single dim >=2 but sum > 2: aggregate
    total = sum(diffs.values())
    if total == 0:
        return {"winner": "TIE", "all_diffs": diffs, "sum_abs": sum_abs, "both_broken": both_broken, "tie_rule": "zero_sum"}
    return {
        "winner": "A" if total > 0 else "B",
        "all_diffs": diffs,
        "sum_abs": sum_abs,
        "total_diff": total,
        "both_broken": both_broken,
        "tie_rule": "aggregate",
    }


def main():
    # Load all integration results
    pairs = []
    for f in sorted(RESULTS.glob("I*.json")):
        data = json.loads(f.read_text())
        integ = data["integration"]
        exa_text = data["exa"].get("content", "") if data["exa"].get("ok") else None
        pplx_text = data["perplexity"].get("content", "") if data["perplexity"].get("ok") else None
        pairs.append({
            "integration": integ,
            "exa_text": exa_text,
            "pplx_text": pplx_text,
        })

    print(f"Judging {len(pairs)*2} playbooks (Shape A absolute)...")
    judge_tasks = []
    for p in pairs:
        if p["exa_text"]:
            judge_tasks.append((p["integration"], "exa", p["exa_text"]))
        if p["pplx_text"]:
            judge_tasks.append((p["integration"], "perplexity", p["pplx_text"]))

    judged = {}
    with cf.ThreadPoolExecutor(max_workers=6) as ex:
        futs = {
            ex.submit(judge_one_playbook, t[0], t[1], t[2]): (t[0]["id"], t[1])
            for t in judge_tasks
        }
        for fut in cf.as_completed(futs):
            iid, provider = futs[fut]
            r = fut.result()
            judged[(iid, provider)] = r
            scores = r.get("scores", {})
            print(f"  {iid}/{provider}: " + (
                f"C&F={scores.get('correctness_and_freshness')} EX={scores.get('executability')} "
                f"CP={scores.get('completeness')} CT={scores.get('citation_anchoring')} ({r.get('_judge_latency_s',0):.1f}s)"
                if scores else f"ERROR: {r.get('error','?')[:80]}"
            ), flush=True)

    # Persist per-playbook judgements
    for (iid, provider), r in judged.items():
        (JUDGE_DIR / f"{iid}-{provider}.json").write_text(json.dumps(r, indent=2))

    # Compute pairwise winners + aggregate
    aggregate = {"pairs": [], "winner_counts": {"exa": 0, "perplexity": 0, "TIE": 0, "skipped": 0},
                 "dim_averages": {"exa": {}, "perplexity": {}}}
    dims = ["correctness_and_freshness", "executability", "completeness", "citation_anchoring"]
    dim_sums = {"exa": {d: [] for d in dims}, "perplexity": {d: [] for d in dims}}

    for p in pairs:
        iid = p["integration"]["id"]
        je = judged.get((iid, "exa"), {})
        jp = judged.get((iid, "perplexity"), {})
        if not (je.get("scores") and jp.get("scores")):
            aggregate["winner_counts"]["skipped"] += 1
            aggregate["pairs"].append({"integration": iid, "skipped": True, "reason": "missing scores"})
            continue
        for d in dims:
            dim_sums["exa"][d].append(je["scores"].get(d, 0))
            dim_sums["perplexity"][d].append(jp["scores"].get(d, 0))
        # Position swap: judge winner two ways: (A=exa, B=pplx) and (A=pplx, B=exa).
        # The local pairwise function is symmetric so we only need to map.
        # A=exa, B=pplx
        verdict = pairwise_winner(je["scores"], jp["scores"])
        winner_provider = {"A": "exa", "B": "perplexity", "TIE": "TIE"}[verdict["winner"]]
        aggregate["winner_counts"][winner_provider] += 1
        aggregate["pairs"].append({
            "integration": iid,
            "name": p["integration"]["name"],
            "exa_scores": je["scores"],
            "pplx_scores": jp["scores"],
            "verdict": verdict,
            "winner_provider": winner_provider,
        })

    for prov in ("exa", "perplexity"):
        for d in dims:
            vals = dim_sums[prov][d]
            aggregate["dim_averages"][prov][d] = round(sum(vals) / len(vals), 2) if vals else None

    (JUDGE_DIR / "_aggregate.json").write_text(json.dumps(aggregate, indent=2))
    print("\n=== JUDGE AGGREGATE ===")
    print(json.dumps(aggregate, indent=2))


if __name__ == "__main__":
    main()
